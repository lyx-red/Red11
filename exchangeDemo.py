# int() 转换成整型
num=23.111
print(int(num))

# float()转换成浮点型
strnum='76'
print(float(strnum))

# str()转换成字符串类型
num2=888
print(str(num2))
print(type(str(num2)))

# tuple()转换成元组类型
b=[2,2,1]
print(tuple(b))

# list()转换成列表
c=(1,2,3)
print(list(c))

# eval() 转换成原来的格式
d='2212'
print(type(eval(d)))

e='[2,3,1]'
print(type(eval(e)))
